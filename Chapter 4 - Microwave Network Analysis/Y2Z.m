function Z = Y2Z(Y)
    Z11 =   ( ( Y(2,2) ) / ... 
            ( (Y(1,1)*Y(2,2)) - (Y(1,2)*Y(2,1)) ) );
    Z12 =   ( ( -Y(1,2) ) / ... 
            ( (Y(1,1)*Y(2,2)) - (Y(1,2)*Y(2,1)) ) );
    Z21 =   ( ( -Y(2,1) ) / ... 
            ( (Y(1,1)*Y(2,2)) - (Y(1,2)*Y(2,1)) ) );
    Z22 =   ( ( Y(1,1) ) / ... 
            ( (Y(1,1)*Y(2,2)) - (Y(1,2)*Y(2,1)) ) );
    Z = [Z11, Z12; Z21, Z22];
end

