function S = Y2S(Y, Z_0)
    Y11 = Y(1,1); Y12 = Y(1,2); Y21 = Y(2,1); Y22 = Y(2,2); Y_0 = 1 / Z_0;
    S11 = - ( ( (Y11-Y_0)*(Y22+Y_0) - Y12*Y21 ) / ... 
            ( (Y11+Y_0)*(Y22+Y_0) - Y12*Y21 ) );
    S12 = - ( ( 2*Y12*Y_0 ) / ... 
            ( (Y11+Y_0)*(Y22+Y_0) - Y12*Y21 ) );
    S21 = - ( ( 2*Y21*Y_0 ) / ... 
            ( (Y11+Y_0)*(Y22+Y_0) - Y12*Y21 ) );
    S22 = - ( ( (Y11+Y_0)*(Y22-Y_0) - Y12*Y21 ) / ... 
            ( (Y11+Y_0)*(Y22+Y_0) - Y12*Y21 ) );
    S = [S11, S12; S21, S22];
end

