function Y = Z2Y(Z)
    Y11 =   ( ( Z(2,2) ) / ... 
            ( (Z(1,1)*Z(2,2)) - (Z(1,2)*Z(2,1)) ) );
    Y12 =   ( ( -Z(1,2) ) / ... 
            ( (Z(1,1)*Z(2,2)) - (Z(1,2)*Z(2,1)) ) );
    Y21 =   ( ( -Z(2,1) ) / ... 
            ( (Z(1,1)*Z(2,2)) - (Z(1,2)*Z(2,1)) ) );
    Y22 =   ( ( Z(1,1) ) / ... 
            ( (Z(1,1)*Z(2,2)) - (Z(1,2)*Z(2,1)) ) );
    Y = [Y11, Y12; Y21, Y22];
end


